package com.algo.util;

public class Utils {
    public static void print1DArray(int[] arr) {
        for (int a : arr) {
            System.out.printf("%d, ", a);
        }
        System.out.println();
    }

    public static void print1DArray(Integer[] arr) {
        for (int a : arr) {
            System.out.printf("%d, ", a);
        }
        System.out.println();
    }
}
